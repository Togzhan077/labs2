#Arithmetic operators +,-,*,/,**,%,//
x=15
y=5

#+
print("+ ")
print(x+y)

#-
print("- ")
print(x-y)

#*
print("* ")
print(x*y)

#/-просто бөлу, толық шығарып береді
print("/ ")
print(x/y)

#**-дәреже
print("** ")
print(x**y)

#%-бөлгендегі қалдығы
print("% ")
print(x%y)

#//-бөлгендегі бүтіні
print("// ")
print(x//y)

